int	ft_sqrt(int nb)
{
	long	i;
	long	lnb;

	lnb = nb;
	i = 1;
	if (nb <= 0)
		return (0);
	while (i * i <= lnb)
	{
		if (i * i == lnb)
			return (i);
		i++;
	}
	return (0);
}
